CREATE TABLE  KHACHHANG
(
	MAKH CHAR(15),
	TEN NCHAR(15),
	DCHI NCHAR(50),
	DTHOAI CHAR(12)

	CONSTRAINT PK_KH
	PRIMARY KEY(MAKH)
)

CREATE TABLE DATCHO
(
	MAKH CHAR(15),
	NGAYDI DATE,
	MACB CHAR(4)


	CONSTRAINT PK_DC
	PRIMARY KEY(MAKH,NGAYDI,MACB)
)

CREATE TABLE LICHBAY
(
	NGAYDI DATE,
	MACB CHAR(4),
	SOHIEU INT,
	MALOAI CHAR(15)

	CONSTRAINT PK_LB
	PRIMARY KEY(NGAYDI,MACB)
)

CREATE TABLE CHUYENBAY
(
	MACB CHAR(4),
	SBDI CHAR(3),
	SBDEN CHAR(3),
	GIODI TIME,
	GIODEN TIME

	CONSTRAINT PK_CB
	PRIMARY KEY(MACB)
)

CREATE TABLE MAYBAY
(
	SOHIEU INT,
	MALOAI CHAR(15)

	CONSTRAINT PK_MB
	PRIMARY KEY(SOHIEU,MALOAI)
)

CREATE TABLE LOAIMB
(
	HANGSX NCHAR(15),
	MALOAI CHAR(15)

	CONSTRAINT PK_LMB
	PRIMARY KEY(MALOAI)
)



CREATE TABLE KHANANG 
(
	MANV CHAR(15),
	MALOAI CHAR(15)

	CONSTRAINT PK_KN
	PRIMARY KEY(MANV,MALOAI)
)

CREATE TABLE NHANVIEN
(	
	MANV CHAR(15),
	TEN NCHAR(15),
	DCHI NCHAR(50),
	DTHOAI CHAR(12),
	LUONG NUMERIC(10,2),
	LOAINV INT CHECK(LOAINV IN (0,1))

	CONSTRAINT PK_NV
	PRIMARY KEY(MANV)

)
CREATE TABLE PHANCONG
(
	MANV CHAR(15),
	NGAYDI DATE,
	MACB CHAR(4)

	CONSTRAINT PK_PC
	PRIMARY KEY(MANV,NGAYDI,MACB)
)

ALTER TABLE DATCHO ADD
	CONSTRAINT FK_DC_KH FOREIGN KEY (MAKH)  REFERENCES KHACHHANG(MAKH),
	CONSTRAINT FK_DC_LB FOREIGN KEY(NGAYDI,MACB)   REFERENCES LICHBAY(NGAYDI,MACB)

ALTER TABLE PHANCONG ADD
	CONSTRAINT FK_PC_LB FOREIGN KEY(NGAYDI,MACB)   REFERENCES LICHBAY(NGAYDI,MACB),
	CONSTRAINT FK_PC_NV FOREIGN KEY(MANV) REFERENCES NHANVIEN(MANV)

ALTER TABLE LICHBAY ADD
	CONSTRAINT FK_LB_CB FOREIGN KEY(MACB)   REFERENCES CHUYENBAY(MACB),
	CONSTRAINT FK_LB_MB FOREIGN KEY(SOHIEU,MALOAI) REFERENCES MAYBAY(SOHIEU,MALOAI)

ALTER TABLE MAYBAY ADD
	CONSTRAINT FK_MB_LMB FOREIGN KEY(MALOAI)   REFERENCES LOAIMB(MALOAI)

ALTER TABLE KHANANG ADD
	CONSTRAINT FK_KN_LMB FOREIGN KEY(MALOAI)   REFERENCES LOAIMB(MALOAI),
	CONSTRAINT FK_KN_NV FOREIGN KEY(MANV)   REFERENCES NHANVIEN(MANV)

